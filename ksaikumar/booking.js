var bus=[[["L1"],["R2"]],[["L3"],["R4"]],[["L5"],["R6"]]];
function booking(gender){   
for(i=0;i<bus.length;i++){
for(j=0;j<bus[i].length;j++){
for(k=0;k<1;k++){  
    if(bus[i][j][k]==undefined){
    if(bus[i][j].push(gender));{
        return bus;  
    }      
}
}
}
 }
}
